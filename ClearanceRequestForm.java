    private void GetAYSem() {
    try (Connection conn = DBConnection.getConnection()) {
        String query = "SELECT DISTINCT t1.acadyear FROM clearancerequest t1 WHERE t1.studentID=? ORDER BY t1.acadyear DESC;";
        PreparedStatement checkpst = conn.prepareStatement(query);
        checkpst.setInt(1, studentID);
        ResultSet rs = checkpst.executeQuery();

        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        model.addElement("All");

        while (rs.next()) {
            String acadyear = rs.getString("acadyear");
            model.addElement(acadyear);
        }

        acadyearcmbbx2.setModel(model);

        rs.close();
        checkpst.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error loading academic years: " + e.getMessage());
    }
}

    
        
        
        
    

    

