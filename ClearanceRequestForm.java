public void GetClearanceRequest() {
    try (Connection conn = DBConnection.getConnection()) {
        String query = "SELECT *, concat(t1.acadyear,'-',t1.semester) as aysem "
                     + "FROM clearancerequest as t1 "
                     + "LEFT JOIN clearancestatus as t2 ON t1.clearancerequestID=t2.clearancerequestID "
                     + "JOIN clearingoffices as t3 ON t3.clearingofficesID=t2.clearingofficesID "
                     + "WHERE t1.studentID=?";
        PreparedStatement pst = conn.prepareStatement(query);
        pst.setInt(1, studentID);
        ResultSet rs = pst.executeQuery();

        // Create table model
        DefaultTableModel col = new DefaultTableModel();
        col.addColumn("Clearing Offices");
        col.addColumn("Academic Year and Sem");
        col.addColumn("Status");
        col.addColumn("Remarks");
        col.addColumn("Date Requested");

        // Populate table rows
        while (rs.next()) {
            col.addRow(new Object[]{
                rs.getString("clearingoffice"),
                rs.getString("aysem"),
                rs.getString("status"),
                rs.getString("remarks"),
                rs.getString("dateRequested")
            });
        }

        // Set model to table
        clearancestatusIDlbl.setModel(col);

        // Instruction Step 4: Apply row coloring based on Status
        clearancestatusIDlbl.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
            public Component getTableCellRendererComponent(JTable table, Object value,
                    boolean isSelected, boolean hasFocus, int row, int column) {
                Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
                String status = table.getValueAt(row, 2) != null ? table.getValueAt(row, 2).toString() : "";
                if (!isSelected) { 
                    if (status.equalsIgnoreCase("Cleared")) {
                        c.setBackground(Color.GREEN);
                        c.setForeground(Color.BLACK); // make text readable
                    } else {
                        c.setBackground(Color.WHITE);
                        c.setForeground(Color.BLACK);
                    }
                }
                return c;
            }
        });

        rs.close();
        pst.close();
        conn.close();

    } catch (SQLException ae) {
        JOptionPane.showMessageDialog(this, "Error!", "Information", JOptionPane.WARNING_MESSAGE);
    }
}


